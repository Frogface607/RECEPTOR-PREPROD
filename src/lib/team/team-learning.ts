export function getRoleDayFocus(roleId: TeamRoleId): RoleDayFocus {
  return ROLE_DAY_FOCUS[roleId];
}

export function listShiftChecklistForRole(roleId: TeamRoleId): string[] {
  return SHIFT_CHECKLIST[roleId];
}

export function listLearningItemsForRole(
  roleId: TeamRoleId,
): TeamLearningItem[] {
  return LEARNING_CATALOG.filter(
    (item) => !item.roles || item.roles.includes(roleId),
  );
}

export function listLearningItems(): TeamLearningItem[] {
  return LEARNING_CATALOG;
}

export function getLearningItem(itemId: string): TeamLearningItem | undefined {
  return LEARNING_CATALOG.find((item) => item.id === itemId);
}

export function getLearningItemByTitle(
  title: string | null | undefined,
): TeamLearningItem | undefined {
  const normalized = title?.trim().toLocaleLowerCase("ru-RU");
  if (!normalized) return undefined;

  return LEARNING_CATALOG.find(
    (item) => item.title.trim().toLocaleLowerCase("ru-RU") === normalized,
  );
}

export function learningModuleHref(
  moduleId: string,
  checklistTitle?: string | null,
): string {
  const params = new URLSearchParams({ module: moduleId });
  const checklist = checklistTitle?.trim();
  if (checklist) params.set("checklist", checklist);
  return `/me/learning?${params.toString()}`;
}

export function calculateLearningScore(
  item: TeamLearningItem,
  answers: readonly number[],
): TeamLearningScore {
  const total = item.quiz.length;
  const correct = item.quiz.reduce(
    (sum, question, index) =>
      answers[index] === question.correctIndex ? sum + 1 : sum,
    0,
  );
  const percentage = total > 0 ? Math.round((correct / total) * 100) : 0;

  return {
    correct,
    total,
    percentage,
    passed: percentage >= item.passPercentage,
  };
}
