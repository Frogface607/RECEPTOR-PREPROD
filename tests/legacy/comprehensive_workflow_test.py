# Test Configuration
BACKEND_URL = os.getenv('REACT_APP_BACKEND_URL', 'https://cursor-push.preview.emergentagent.com')
API_BASE = f"{BACKEND_URL}/api/v1"
MONGO_URL = os.getenv('MONGO_URL', 'mongodb://localhost:27017/receptor_pro')
DB_NAME = os.getenv('DB_NAME', 'receptor_pro')

class ComprehensiveWorkflowTester:
    """Comprehensive workflow test focusing on the complete system"""
    
    def __init__(self):
        self.client = httpx.AsyncClient(timeout=60.0)
        self.test_results = []
        self.generated_techcard_id = None
        self.preflight_data = None
        
    async def __aenter__(self):
        return self
        
    async def __aexit__(self, exc_type, exc_val, exc_tb):
        await self.client.aclose()
    
    def log_test(self, test_name: str, success: bool, details: str = "", response_time: float = 0.0):
        """Log test result"""
        status = "✅ PASS" if success else "❌ FAIL"
        result = {
            "test": test_name,
            "status": status,
            "success": success,
            "details": details,
            "response_time": f"{response_time:.3f}s" if response_time > 0 else "N/A"
        }
        self.test_results.append(result)
        print(f"{status} {test_name}: {details}")
        return success

    async def test_1_generate_techcard(self) -> bool:
        """1. Generate tech card 'Суп Том Ям'"""
        start_time = time.time()
        
        try:
            generate_url = f"{API_BASE}/techcards.v2/generate"
            payload = {
                "name": "Суп Том Ям",
                "description": "Острый тайский суп с креветками, грибами и лемонграссом"
            }
            
            response = await self.client.post(generate_url, json=payload)
            response_time = time.time() - start_time
            
            if response.status_code != 200:
                return self.log_test(
                    "Generate TechCard", 
                    False, 
                    f"HTTP {response.status_code}: {response.text[:200]}", 
                    response_time
                )
            
            data = response.json()
            
            if 'card' in data and 'meta' in data['card']:
                self.generated_techcard_id = data['card']['meta']['id']
                
                # Check LLM pipeline article generation
                timings = data['card']['meta'].get('timings', {})
                article_gen_time = timings.get('article_generation_ms', 0)
                
                # Check if articles were generated by LLM
                dish_article = data['card'].get('article') or data['card']['meta'].get('article')
                ingredients_with_codes = 0
                total_ingredients = len(data['card'].get('ingredients', []))
                
                for ingredient in data['card'].get('ingredients', []):
                    if ingredient.get('product_code'):
                        ingredients_with_codes += 1
                
                details = f"ID: {self.generated_techcard_id}, "
                details += f"Article gen time: {article_gen_time}ms, "
                details += f"LLM dish article: {dish_article}, "
                details += f"LLM ingredient codes: {ingredients_with_codes}/{total_ingredients}"
                
                return self.log_test(
                    "Generate TechCard", 
                    True, 
                    details, 
                    response_time
                )
            else:
                return self.log_test(
                    "Generate TechCard", 
                    False, 
                    "Invalid response structure", 
                    response_time
                )
                
        except Exception as e:
            return self.log_test(
                "Generate TechCard", 
                False, 
                f"Exception: {str(e)}", 
                time.time() - start_time
            )

    async def test_2_verify_db_storage(self) -> bool:
        """2. Verify tech card is stored in database"""
        
        if not self.generated_techcard_id:
            return self.log_test("Verify DB Storage", False, "No techcard ID")
        
        try:
            client = MongoClient(MONGO_URL)
            db = client[DB_NAME]
            
            techcard = db.techcards_v2.find_one({'meta.id': self.generated_techcard_id})
            client.close()
            
            if not techcard:
                return self.log_test("Verify DB Storage", False, "TechCard not found in DB")
            
            # Check database content
            db_dish_article = techcard.get('article') or techcard.get('meta', {}).get('article')
            db_ingredients_with_codes = 0
            db_total_ingredients = len(techcard.get('ingredients', []))
            
            for ingredient in techcard.get('ingredients', []):
                if ingredient.get('product_code'):
                    db_ingredients_with_codes += 1
            
            details = f"Found in DB, "
            details += f"DB dish article: {db_dish_article}, "
            details += f"DB ingredient codes: {db_ingredients_with_codes}/{db_total_ingredients}"
            
            return self.log_test("Verify DB Storage", True, details)
            
        except Exception as e:
            return self.log_test("Verify DB Storage", False, f"Exception: {str(e)}")

    async def test_3_preflight_article_generation(self) -> bool:
        """3. Test preflight article generation"""
        
        if not self.generated_techcard_id:
            return self.log_test("Preflight Article Generation", False, "No techcard ID")
        
        start_time = time.time()
        
        try:
            preflight_url = f"{API_BASE}/export/preflight"
            payload = {"techcardIds": [self.generated_techcard_id]}
            
            response = await self.client.post(preflight_url, json=payload)
            response_time = time.time() - start_time
            
            if response.status_code != 200:
                return self.log_test(
                    "Preflight Article Generation", 
                    False, 
                    f"HTTP {response.status_code}: {response.text[:200]}", 
                    response_time
                )
            
            self.preflight_data = response.json()
            
            # Analyze preflight results
            generated_dish_articles = self.preflight_data.get('generated', {}).get('dishArticles', [])
            generated_product_articles = self.preflight_data.get('generated', {}).get('productArticles', [])
            
            missing_dishes = self.preflight_data.get('missing', {}).get('dishes', [])
            missing_products = self.preflight_data.get('missing', {}).get('products', [])
            
            # Validate 5-digit format
            valid_dish_articles = [a for a in generated_dish_articles if len(str(a)) == 6 and str(a).isdigit()]
            valid_product_articles = [a for a in generated_product_articles if len(str(a)) == 6 and str(a).isdigit()]
            
            success = (
                len(generated_dish_articles) > 0 and 
                len(generated_product_articles) > 0 and
                len(valid_dish_articles) > 0 and
                len(valid_product_articles) > 0
            )
            
            details = f"Dish articles: {generated_dish_articles}, "
            details += f"Product articles: {generated_product_articles[:3]}..., "
            details += f"Missing dishes: {len(missing_dishes)}, "
            details += f"Missing products: {len(missing_products)}"
            
            return self.log_test(
                "Preflight Article Generation", 
                success, 
                details, 
                response_time
            )
            
        except Exception as e:
            return self.log_test(
                "Preflight Article Generation", 
                False, 
                f"Exception: {str(e)}", 
                time.time() - start_time
            )

    async def test_4_export_with_skeleton_generation(self) -> bool:
        """4. Test export with skeleton generation"""
        
        if not self.generated_techcard_id or not self.preflight_data:
            return self.log_test("Export with Skeleton Generation", False, "Missing prerequisites")
        
        start_time = time.time()
        
        try:
            # Try export that should generate skeletons
            export_url = f"{API_BASE}/export/zip"
            payload = {
                "techcardIds": [self.generated_techcard_id],
                "include_skeletons": True,
                "force_skeleton_generation": True
            }
            
            response = await self.client.post(export_url, json=payload)
            response_time = time.time() - start_time
            
            if response.status_code == 400:
                # This is expected - system correctly blocks TTK-only export
                error_data = response.json()
                if 'PRE_FLIGHT_REQUIRED' in str(error_data):
                    return self.log_test(
                        "Export with Skeleton Generation", 
                        True, 
                        "System correctly blocks TTK-only export and requires skeleton import first", 
                        response_time
                    )
            
            if response.status_code == 200:
                # Check if we got a ZIP file
                zip_size = len(response.content)
                if zip_size > 1000:  # Valid ZIP should be larger
                    self.exported_zip_content = response.content
                    return self.log_test(
                        "Export with Skeleton Generation", 
                        True, 
                        f"ZIP export successful: {zip_size} bytes", 
                        response_time
                    )
            
            return self.log_test(
                "Export with Skeleton Generation", 
                False, 
                f"Unexpected response: HTTP {response.status_code}: {response.text[:200]}", 
                response_time
            )
            
        except Exception as e:
            return self.log_test(
                "Export with Skeleton Generation", 
                False, 
                f"Exception: {str(e)}", 
                time.time() - start_time
            )

